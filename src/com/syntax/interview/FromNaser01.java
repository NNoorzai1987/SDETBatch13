package com.syntax.interview;

public class FromNaser01 {
    public static void main(String[] args) {

        String str="I am learning Java";
        int num=0;
        for (int i = 0; i < str.length(); i++) {
            char ch=str.charAt(i);
            if (ch==' ');
            num++;
        }
        System.out.println(num);
    }
}
